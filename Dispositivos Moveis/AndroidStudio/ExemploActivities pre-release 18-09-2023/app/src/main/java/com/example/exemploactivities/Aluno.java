package com.example.exemploactivities;

public class Aluno {
    private int ra;
    private String nome;

    public Aluno() {
        this.ra = ra;
        this.nome = nome;
    }

    //Getters
    public int getRa() {
        return ra;
    }

    public String getNome() {
        return nome;
    }

    //Setters

    public void setRa(int ra) {
        this.ra = ra;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}